function enter_to_focus(event, element){
		var code = 0;

		if (document.layers)
		  code = event.which;
		else
		  code = event.keyCode;
		if (code==13)	element.focus();
}

function enter_to_submit(event){
		var code = 0;

		if (document.layers)
		  code = event.which;
		else
		  code = event.keyCode;
		if (code==13)	document.forms[0].submit();
}

